function levenshtein(a: string, b: string): number {
  if (Math.abs(a.length - b.length) > 3) return 99;
  const dp: number[][] = Array.from({ length: a.length + 1 }, (_, i) =>
    Array.from({ length: b.length + 1 }, (_, j) => (i === 0 ? j : j === 0 ? i : 0))
  );
  for (let i = 1; i <= a.length; i++)
    for (let j = 1; j <= b.length; j++)
      dp[i][j] = a[i-1] === b[j-1] ? dp[i-1][j-1] : 1 + Math.min(dp[i-1][j], dp[i][j-1], dp[i-1][j-1]);
  return dp[a.length][b.length];
}

function correctWord(word: string, dict: string[]): string {
  if (word.length < 3) return word;
  const lw = word.toLowerCase();
  const exact = dict.find(d => d.toLowerCase() === lw);
  if (exact) return exact;
  let best = word, bestDist = 3;
  for (const d of dict) {
    if (d[0].toLowerCase() !== lw[0]) continue;
    const dist = levenshtein(lw, d.toLowerCase());
    if (dist < bestDist) { bestDist = dist; best = d; }
  }
  return bestDist <= 2 ? best : word;
}

export function correctNameSpelling(text: string): string {
  const words = text.trim().split(/\s+/);
  return words.map((w, i) => {
    if (!w) return w;
    if (i === 0) return w; // surname — skip
    if (i === 1) return correctWord(w, UA_FIRST_NAMES);
    if (i === 2) return correctWord(w, UA_PATRONYMICS);
    return w;
  }).join(" ");
}
