// Builds greeting text for Block 5 (sent from bot on /start registration)
export function buildGreetingText(
  name: string,
  patronymic: string | null,
  procedure: string,
  visitDate: string,
  visitTime: string | null
): string {
  const addr = buildAddress(name, patronymic);
  // Format visit date: YYYY-MM-DD → DD.MM.YYYY
  const [y, m, d] = visitDate.split("-");
  const dateDisplay = `${d}.${m}.${y}`;
  const timeDisplay = visitTime || "";
  return md(
    `Вітаю, **${addr}**!\n` +
    `Це цифровий асистент лікаря **Луцишина Юрія Андрійовича**.\n` +
    `Ви записані на процедуру: **${escapeHtml(procedure)}**, яка відбудеться **${escapeHtml(dateDisplay)}**` +
    (timeDisplay ? ` о **${escapeHtml(timeDisplay)}**` : "") + `.\n` +
    `Юрій Андрійович доручив мені супроводжувати вашу підготовку. Моє завдання — допомогти вам пройти цей етап правильно та спокійно, щоб процедура пройшла легко та з найкращим результатом.\n` +
    `Чи готові ви розпочати нашу спільну підготовку?`
  );
}

