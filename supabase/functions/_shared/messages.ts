// Escapes characters that break Telegram HTML mode.
// Must be applied to all dynamic values (names, procedure titles, dates)
// before embedding them in HTML-tagged strings — otherwise Telegram rejects
// the entire message and displays raw tags as literal text.
function escapeHtml(s: string): string {
  return s.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;");
}

// Converts **bold** and ***bold-italic*** markdown to Telegram HTML tags
function md(text: string): string {
  return text
    .replace(/\*\*\*([^*]+)\*\*\*/g, "<b><i>$1</i></b>")
    .replace(/\*\*([^*]+)\*\*/g, "<b>$1</b>");
}

// Reverses md() + escapeHtml() for dashboard storage.
// assistant_chats uses **markdown**, Telegram uses HTML — these are separate.
export function stripHtml(text: string): string {
  return text
    .replace(/<b><i>([^<]+)<\/i><\/b>/g, "***$1***")
    .replace(/<b>([^<]+)<\/b>/g, "**$1**")
    .replace(/&amp;/g, "&")
    .replace(/&lt;/g, "<")
    .replace(/&gt;/g, ">");
}

// Returns "Пане Іване Петровичу" / "Пані Наталіє Іванівно" (HTML-escaped)
export function buildAddress(name: string, patronymic: string | null): string {
  const parts = (name || "").trim().split(/\s+/);
  const firstName = parts[1] ?? parts[0] ?? "Пацієнте";
  const patron = (patronymic || "").trim();
  const isFemale = /івна$|ївна$|вна$/i.test(patron);
  const sal = isFemale ? "Пані" : "Пане";
  const raw = patron ? `${sal} ${firstName} ${patron}` : `${sal} ${firstName}`;
  return escapeHtml(raw);
}

// Clinic address based on day of week of the visit date
export function getClinicAddress(visitDate: string): string {
  // visitDate is YYYY-MM-DD in UTC; use UTC day to avoid timezone flip
  const dow = new Date(visitDate + "T00:00:00Z").getUTCDay();
  if (dow === 3) return "вул. Юрія Руфа 35в, Центр «МеДіС»";
  if (dow === 6) return ""; // Saturday — special arrangement, no address sent
  return "вул. Пекарська 696, 2 поверх, 231 каб.";
}

// callback_data: "{context}:{answer}:{visitId}"
function cb(context: string, answer: string, visitId: string): string {
  return `${context}:${answer}:${visitId}`;
}

